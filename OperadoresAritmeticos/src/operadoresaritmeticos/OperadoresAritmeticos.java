package operadoresaritmeticos;

/**
 *
 * @author Maribel
 */
public class OperadoresAritmeticos {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int variableX = 50;
        int variableY = 10;
        int resultado = 0;
        
        resultado = variableX + variableY;
        System.out.println(resultado);
        
        resultado = variableX - variableY;
        System.out.println(resultado);
        
        resultado = variableX * variableY;
        System.out.println(resultado);
        
        resultado = variableX / variableY;
        System.out.println(resultado);
    }
    
}
